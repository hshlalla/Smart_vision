const I18nContext = createContext<I18nContextValue | null>(null);

function resolveInitialLanguage(): Language {
  const saved = localStorage.getItem(LANGUAGE_KEY);
  if (saved === "ko" || saved === "en") return saved;
  return "en";
}

function getValue(target: unknown, path: string): string | null {
  const parts = path.split(".");
  let current: unknown = target;
  for (const part of parts) {
    if (!current || typeof current !== "object" || !(part in current)) {
      return null;
    }
    current = (current as Record<string, unknown>)[part];
  }
  return typeof current === "string" ? current : null;
}

function formatMessage(template: string, vars?: TranslationVars): string {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, key) => String(vars[key] ?? ""));
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>(resolveInitialLanguage);

  useEffect(() => {
    localStorage.setItem(LANGUAGE_KEY, language);
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<I18nContextValue>(() => {
    const t = (path: string, vars?: TranslationVars) => {
      const template = getValue(messages[language], path) ?? getValue(messages.en, path) ?? path;
      return formatMessage(template, vars);
    };

    return {
      language,
      setLanguage,
      t,
      statusLabel(status: string) {
        const translated = getValue(messages[language], `status.${status}`) ?? getValue(messages.en, `status.${status}`);
        return translated || status;
      },
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("I18nContext not initialized");
  return ctx;
}
